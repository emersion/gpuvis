uint32_t hashstr32( const char *str, size_t len, uint32_t hval )
{
    if ( len == (size_t)-1 )
        len = strlen( str );

#if 1
    return fnv_32_buf( str, len, hval );
#else
    return MurmurHash3_x86_32( str, ( int )len, hval );
#endif
}

uint32_t hashstr32( const std::string &str, uint32_t hval )
{
#if 1
    return fnv_32_buf( str.c_str(), str.length(), hval );
#else
    return MurmurHash3_x86_32( str.c_str(), ( int )str.length(), hval );
#endif
}

uint64_t hashstr64( const char *str, size_t len )
{
    if ( len == (size_t)-1 )
        len = strlen( str );

    return fnv_64_buf( str, len, FNV1A_64_INIT );
}

uint64_t hashstr64( const std::string &str )
{
    return fnv_64_buf( str.c_str(), str.length(), FNV1A_64_INIT );
}

